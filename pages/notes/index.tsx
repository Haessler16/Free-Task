import Head from 'next/head'
import { MainLayout } from 'layouts/main'
import { NotesList } from 'components/Notes/List'
import { iNote } from 'utils/interfaces/notes'
import {
  Box,
  Text,
  Grid,
  useMediaQuery,
  Spinner,
  Center,
} from '@chakra-ui/react'
import { useState } from 'react'
import { FormToNotes } from 'components/Notes/Form'

import { AddButton } from 'components/AddButton'
import { getSession, GetSessionParams, useSession } from 'next-auth/react'

import { NextPage } from 'next'
import { NotesHeader } from 'components/Notes/Header'
import { iUser } from 'utils/interfaces/user'

import useSWR from 'swr'
import fetcher from 'utils/fetcher'
import prisma from 'lib/prisma'

export async function getServerSideProps(
  context: GetSessionParams | undefined,
) {
  const session = await getSession(context)

  if (!session) {
    return {
      redirect: {
        destination: '/',
        permanent: false,
      },
    }
  }
  const user = session.user as iUser
  const notes = await prisma.notes.findMany({
    where: {
      userId: user.id,
    },
  })

  return {
    props: {
      notes: JSON.parse(JSON.stringify(notes)),
    },
  }
}

const Notes: NextPage<{ notes: iNote[] }> = ({ notes }) => {
  const { data: session } = useSession()

  const [showForm, setShowForm] = useState(false)
  const [isLessThan800] = useMediaQuery('(max-width: 760px)', {
    ssr: true,
    fallback: false, // return false on the server, and re-evaluate on the client side
  })

  const { data } = useSWR<iNote[]>(
    session ? `/api/notes?userId=${(session?.user as iUser).id}` : null,
    fetcher,
    {
      fallbackData: notes,
    },
  )

  const addNotesForm = () => {
    setShowForm(true)
  }

  if (!data || !session) {
    return (
      <Center h='100vh'>
        <Spinner
          thickness='4px'
          speed='0.65s'
          emptyColor='gray.200'
          color='blue.500'
          size='xl'
        />
      </Center>
    )
  }

  console.log({ data })
  return (
    <MainLayout>
      <Head>
        <title>Notes</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/asignacion.png' />
      </Head>

      <main>
        {!showForm ? (
          <Box px={5}>
            <NotesHeader
              isLessThan800={isLessThan800}
              addNotesForm={addNotesForm}
            />

            {data.length > 0 ? (
              <Grid
                id='characters_grid'
                templateColumns='repeat(auto-fit, minmax(min(100%, 22rem), 1fr))'
                gap={2}>
                {data.map((note) => {
                  return (
                    <NotesList
                      key={note.id}
                      note={note}
                      user={session?.user as iUser}
                    />
                  )
                })}
              </Grid>
            ) : (
              <Text textAlign='center' fontSize='lg'>
                No Notes, try to create a new one.
              </Text>
            )}

            {isLessThan800 && <AddButton handleAdd={addNotesForm} />}
          </Box>
        ) : (
          <FormToNotes
            setShowForm={setShowForm}
            user={session?.user as iUser}
          />
        )}
      </main>
    </MainLayout>
  )
}

export default Notes
